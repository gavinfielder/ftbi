#include <stdlib.h>
#include "ftbf.h"

void		ftbf_del(t_ftbf **f)
{
	if (f == NULL || *f == NULL)
		return ;
	ftbi_del(&((*f)->val));
	free(*f);
	*f = NULL;
}
